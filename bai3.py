n=int(input("Nhập n: "))
print("=================")
for i in range(0,n+1):            #Cho i chạy từ 0 -> n 
    print(i)                      #với mỗi giá trị của i thì in ra màn hình
print("=================")
for i in range(0,n+1,2):          #Cho i chạy từ 0 -> n và step = 2
    print(i)                      #với mỗi giá trị của i thì in ra màn hình